    private void populaComboBoxFuncionario() {
        try{
            PreparedStatement ps = ConexaoBD.getConexaoBD().prepareStatement("SELECT CpfFuncionario   FROM Funcionario ");     
                ResultSet rs = ps.executeQuery();
                while(rs.next()){
                    funcionarioBox.addItem(rs.getString("CpfFuncionario"));
                }
        }catch(SQLException ex){
           JOptionPane.showMessageDialog(null, ex);
        }
    }
    
    private void populaComboBoxModelo() {
        try{
            PreparedStatement ps = ConexaoBD.getConexaoBD().prepareStatement("SELECT Modelo   FROM Veiculo ");     
                ResultSet rs = ps.executeQuery();
                while(rs.next()){
                    modeloBox.addItem(rs.getString("Modelo"));
                }
        }catch(SQLException ex){
           JOptionPane.showMessageDialog(null, ex);
        }
    }       
        
