    void onClickCadastrar(){
        VeiculoModel v = new VeiculoModel();
        v.setCor(cor.getText());
        v.setKm(Integer.parseInt(km.getText()));
        v.setModelo(modeloBox.getSelectedItem().toString());
        v.setMontadora(montadora.getText());
        v.setPlaca(placa.getText());
        if(venda.isSelected())
            v.setSituacao(1);
        else if (aluga.isSelected())
            v.setSituacao(2);
        else if(aluga.isSelected())
            v.setSituacao(3);
        v.setTipo(tipo.getText());
        v.save();
    }
    
