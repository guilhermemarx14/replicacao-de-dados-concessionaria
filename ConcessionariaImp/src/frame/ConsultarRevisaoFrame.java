    void onClickidRevisaoBox(){
        RevisaoModel r = RevisaoModel.get(Integer.parseInt(idRevisaoBox.getSelectedItem().toString()));
        cliente.setText(ClienteModel.get(r.getCliente().getIdCliente()).getNome());
        mecanico.setText(r.getMecanico().getCpfFuncionario());
        veiculo.setText("" + r.getVeiculo().getIdVeiculo());
        data.setText(r.getDataRevisao());
        if(r.isEfetivado()){
            situacao.setText("REVISADO");
        }else{
            situacao.setText("NAO REVISAO");
        }
        valor.setText("" + r.getValorConserto());
        valorR.setText(""+r.getValorRevisao());
        
    }
    
