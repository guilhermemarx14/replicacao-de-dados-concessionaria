    private void populaComboBox() {
        try{
            PreparedStatement ps = ConexaoBD.getConexaoBD().prepareStatement("SELECT idRevisao FROM Revisao ");     
                ResultSet rs = ps.executeQuery();
                while(rs.next()){
                    idRevisaoBox.addItem(rs.getString("IdRevisao"));
                }
            }catch(SQLException ex){
                JOptionPane.showMessageDialog(null, ex);
            }
    }

