    @SuppressWarnings("empty-statement")
    private void clienteBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_clienteBoxActionPerformed
      if(clienteBox.getSelectedIndex()!=-1)
      {
          FuncionarioModel fm = FuncionarioModel.get(clienteBox.getSelectedItem().toString());
          MecanicoModel mecanico = MecanicoModel.get(clienteBox.getSelectedItem().toString());
          VendedorModel vendedor =VendedorModel.get(clienteBox.getSelectedItem().toString());
          if(mecanico != null)
          {
               queryFuncaoFunc.setText("Mecanico");
               String a = "" + mecanico.getComissaoPorRevisao();
               queryComissaoFunc.setText(a);
          }else 
          {
              queryFuncaoFunc.setText("Vendedor");
              String a = "" + vendedor.getPorcentagemComissao();
               queryComissaoFunc.setText(a);
          }
          queryNomeFunc.setText(fm.getNome());
          queryTelefoneFunc.setText(fm.getTelefone());
          String b = "" + fm.getSalarioFixo();
          querySalarioFunc.setText(b);
         
      }
    }//GEN-LAST:event_clienteBoxActionPerformed

