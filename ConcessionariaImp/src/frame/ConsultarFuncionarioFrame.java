    private void populaComboBox() {
try{
    PreparedStatement ps = ConexaoBD.getConexaoBD().prepareStatement("SELECT  cpfFuncionario   FROM Funcionario ");     
        ResultSet rs = ps.executeQuery();
        while(rs.next())
        {
            clienteBox.addItem(rs.getString("cpfFuncionario"));
            
        }
}catch(SQLException ex){
   JOptionPane.showMessageDialog(null, ex);
}
        
       
       
        

    }

