    void onClickCadastrar(){
        RevisaoModel r = new RevisaoModel();
        //r.setIdRevisao(0);
        r.setCliente(ClienteModel.get(Integer.parseInt(clienteBox.getSelectedItem().toString())));
        r.setDataRevisao(data.getText());
        r.setEfetivado(true);
        r.setMecanico(MecanicoModel.get(mecanicoBox.getSelectedItem().toString()));
        r.getMecanico().setCpfFuncionario(mecanicoBox.getSelectedItem().toString());
        r.setValorConserto(Float.parseFloat(valorConserto.getText()));
        r.setValorRevisao(Float.parseFloat(valor.getText()));
        r.setVeiculo(VeiculoModel.get(Integer.parseInt(carroBox.getSelectedItem().toString())));
       
        r.save();
    }
