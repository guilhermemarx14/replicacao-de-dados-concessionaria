    private void populaComboBoxCliente() {
        try{
            PreparedStatement ps = ConexaoBD.getConexaoBD().prepareStatement("SELECT IdCliente FROM Cliente ");     
                ResultSet rs = ps.executeQuery();
                while(rs.next()){
                    clienteBox.addItem(rs.getString("IdCliente"));
                }
            }catch(SQLException ex){
                JOptionPane.showMessageDialog(null, ex);
            }
    }
    private void populaComboBoxMecanico() {
        try{
            PreparedStatement ps = ConexaoBD.getConexaoBD().prepareStatement("SELECT cpfFuncionario FROM Mecanico ");     
                ResultSet rs = ps.executeQuery();
                while(rs.next()){
                    mecanicoBox.addItem(rs.getString("cpfFuncionario"));
                }
            }catch(SQLException ex){
                JOptionPane.showMessageDialog(null, ex);
            }
    }
    private void populaComboBoxCarro() {
        try{
            PreparedStatement ps = ConexaoBD.getConexaoBD().prepareStatement("SELECT idVeiculo FROM Veiculo ");     
                ResultSet rs = ps.executeQuery();
                while(rs.next()){
                    carroBox.addItem(rs.getString("IdVeiculo"));
                }
            }catch(SQLException ex){
                JOptionPane.showMessageDialog(null, ex);
            }
    }

